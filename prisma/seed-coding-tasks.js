async function main() {
  console.log("Adăugare coding tasks la lecțiile existente...");
  let added = 0;
  let skipped = 0;

  for (const [lessonSlug, tasks] of Object.entries(CODING_TASKS)) {
    const lesson = await prisma.lesson.findFirst({ where: { slug: lessonSlug } });
    if (!lesson) {
      console.log(`  [skip] Lecție negăsită: ${lessonSlug}`);
      skipped++;
      continue;
    }

    // Check existing max task number
    const existing = await prisma.task.findMany({
      where: { lessonId: lesson.id },
      orderBy: { number: "desc" },
      take: 1,
    });
    const maxNumber = existing[0]?.number ?? 0;

    // Count existing coding tasks
    const existingCoding = await prisma.task.count({
      where: { lessonId: lesson.id, type: "coding" },
    });

    if (existingCoding >= 3) {
      console.log(`  [skip] ${lessonSlug} — are deja ${existingCoding} coding tasks`);
      skipped++;
      continue;
    }

    // Renumber tasks starting after existing max
    let nextNumber = maxNumber + 1;
    for (const task of tasks) {
      await prisma.task.create({
        data: {
          lessonId: lesson.id,
          number: nextNumber++,
          name: task.name,
          question: task.question,
          options: task.options || [],
          answer: task.answer || "",
          explanation: task.explanation || "",
          difficulty: task.difficulty || "easy",
          type: "coding",
          language: task.language || "javascript",
          starterCode: task.starterCode || "",
          expectedOutput: task.expectedOutput || "",
        },
      });
      added++;
    }
    console.log(`  [ok] ${lessonSlug} — ${tasks.length} coding tasks adăugate (numere ${maxNumber + 1}-${nextNumber - 1})`);
  }

  console.log(`\nGata: ${added} tasks adăugate, ${skipped} lecții sărite.`);
}

main()
  .catch(e => { console.error(e); process.exit(1); })
  .finally(() => prisma.$disconnect());
